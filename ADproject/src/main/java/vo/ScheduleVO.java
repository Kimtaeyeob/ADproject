package vo;

public class ScheduleVO {
    private int academyIdx;
    private String academyName;
    private String academyAddress;
    private String academyCategory;
    private String classDay;
    private String classTime;
    private String className;
    private int instructorIdx;
    private String instructorName;

    // Getters and Setters
    public int getAcademyIdx() {
        return academyIdx;
    }

    public void setAcademyIdx(int academyIdx) {
        this.academyIdx = academyIdx;
    }

    public String getAcademyName() {
        return academyName;
    }

    public void setAcademyName(String academyName) {
        this.academyName = academyName;
    }

    public String getAcademyAddress() {
        return academyAddress;
    }

    public void setAcademyAddress(String academyAddress) {
        this.academyAddress = academyAddress;
    }

    public String getAcademyCategory() {
        return academyCategory;
    }

    public void setAcademyCategory(String academyCategory) {
        this.academyCategory = academyCategory;
    }

    public String getClassDay() {
        return classDay;
    }

    public void setClassDay(String classDay) {
        this.classDay = classDay;
    }

    public String getClassTime() {
        return classTime;
    }

    public void setClassTime(String classTime) {
        this.classTime = classTime;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public int getInstructorIdx() {
        return instructorIdx;
    }

    public void setInstructorIdx(int instructorIdx) {
        this.instructorIdx = instructorIdx;
    }

    public String getInstructorName() {
        return instructorName;
    }

    public void setInstructorName(String instructorName) {
        this.instructorName = instructorName;
    }
}
